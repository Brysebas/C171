package B4;

public class Estudiante {


    String nombre;
    String apellido;
    String telefono;
    String cedula;
    double promedio;
    String carrera;

    Estudiante(String nombre, String apellido, String telefono, String cedula, double promedio, String carrera) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
        this.cedula = cedula;
        this.promedio = promedio;
        this.carrera = carrera;
    }
}
